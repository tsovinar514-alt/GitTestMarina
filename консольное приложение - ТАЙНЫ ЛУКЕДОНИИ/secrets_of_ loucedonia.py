# --- НАСТРОЙКА СЛОЖНОСТИ (Усложнение игры) ---
print("Выберите уровень сложности:")
print("1 — Легкий (Числа 1-10, Франкенштейн дает подсказки, 5 жизней)")
print("2 — Сложный (Числа 1-50, Рейзел смотрит на вас, 6 жизней)")

while True:
    choice = input("Введите номер уровня (1 или 2): ").strip()
    if choice == '1':
        max_number = 10
        lives = 5
        break
    elif choice == '2':
        max_number = 50
        lives = 6
        break
    else:
        print("Пожалуйста, введите только цифру 1 или 2.")

secret_number = random.randint(1, max_number)
print(f"\n[Система]: Загадано число от 1 до {max_number}. У вас есть {lives} жизней.")

while lives > 0:
    try:
        user_guess = int(input("\nВведите ваше число: "))
        
        if user_guess == secret_number:
            print(f"\n🎉 ПОБЕДА! Кадис Этрама Д. Рейзел признает вашу силу!")
            print(f"Вы угадали число! Секретный код действительно был: {secret_number}")
            break
        else:
            lives -= 1
            print(f"❌ Неверно! Теряется 1 жизнь. Осталось жизней: {lives}")
            
            if lives > 0:
                if user_guess < secret_number:
                    print("[Подсказка Франкенштейна]: Моё число БОЛЬШЕ вашего.")
                else:
                    print("[Подсказка Франкенштейна]: Моё число МЕНЬШЕ вашего.")
                    
    except ValueError:
        print("⚠ Ошибка! Франкенштейн ругается: вводить можно только ЦЕЛЫЕ числа!")

if lives == 0:
    print(f"\n💀 ИГРА ОКОНЧЕНА. Жизни закончились. Эксперимент провален!")
    print(f"Загаданный код был: {secret_number}. Попробуйте в следующий раз!")
